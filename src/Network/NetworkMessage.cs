    internal static ushort GenerateRequestId(ref int requestId)
    {
        while (true)
        {
            int current = requestId;
            int next = current >= ushort.MaxValue ? 1 : current + 1;

            if (Interlocked.CompareExchange(ref requestId, next, current) == current)
                return (ushort)next;
        }
